package org.hillel;

public class Calculator {

    public int multiplyTest(int a, int b) {
        return a * b;
    }

    public int divideTest(int a, int b) throws ArithmeticException {
        return a / b;
    }

    public int addTest(int a, int b) {
        return a + b;
    }

    public int subtractTest(int a, int b) {
        return a - b;
    }
}